// Soal Prioritas 1 (Nilai 80)
// Tugas Percabangan (Branching)
void main() {
  int nilai = 0;
  if (nilai > 70) {
    print("Nilai $nilai adalah A");
  } else if (nilai > 40) {
    print("Nilai $nilai adalah B");
  } else if (nilai > 0) {
    print("Nilai $nilai adalah C");
  } else {
    print(" ");
  }
}
